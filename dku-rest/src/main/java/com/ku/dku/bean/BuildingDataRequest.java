package com.ku.dku.bean;

public class BuildingDataRequest {
		private long recIdRequest;

		
		
		public BuildingDataRequest() {
			super();
		}

		public BuildingDataRequest(long recIdRequest) {
			super();
			this.recIdRequest = recIdRequest;
		}

		public long getRecIdRequest() {
			return recIdRequest;
		}

		public void setRecIdRequest(long recIdRequest) {
			this.recIdRequest = recIdRequest;
		}
		
		
}
