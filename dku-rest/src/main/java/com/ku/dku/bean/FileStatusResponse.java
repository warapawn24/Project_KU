package com.ku.dku.bean;

public class FileStatusResponse {

	private String status;

	
	public FileStatusResponse() {
		super();
	}

	public FileStatusResponse(String status) {
		super();
		this.status = status;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
